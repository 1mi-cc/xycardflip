"""
虚拟商品专门策略
和实体商品完全不同
"""

class VirtualGoodsStrategy:
    """虚拟商品策略"""
    
    # 虚拟商品的关键指标
    KEY_METRICS = {
        'turnover_time': 'HOURS',       # 不是天，是小时甚至分钟
        'inventory': 'UNLIMITED',        # 虚拟库存无限
        'fulfillment': 'INSTANT',       # 秒发货
        'return_rate': 'VERY_LOW',      # 虚拟产品几乎不退货
        'profit_margin': 'HIGH',         # 50-300%
    }
    
    # 虚拟商品的买入逻辑
    BUYING_LOGIC = """
    虚拟点卡：
    - 不需要评分检查（产品本身是硬需求）
    - 只需要检查：便宜幅度 > 2%
    - 立即买入，立即上架
    
    游戏账号：
    - 需要检查：账号级别、皮肤数量、战绩
    - 需要计算：成本 + 加强费 vs 市价
    - 加强好后再上架
    """
    
    # 虚拟商品的定价逻辑（和实体商品不同）
    PRICING_LOGIC = """
    点卡：
    - 官方价 = ¥100
    - 闲鱼价 = ¥98-102（便宜 1-2% 最容易卖）
    - 永远比官方便宜 1-3%
    - 秒成交（市场容量大）
    
    账号：
    - 定价 = 成本 × 1.5-2.0
    - 看同类账号价格，往高了定
    - 不容易卖出，但利润高
    - 要有耐心等
    """
    
    # 虚拟商品的风险（很不同）
    RISKS = {
        '点卡': '低（产品本身没问题，只是价差套利）',
        '账号': '中（买家可能发现被骗，闲鱼平台管制）',
        '共享': '高（违反服务条款，账号易被封）',
        '授权': '高（微软/Adobe 检测假序列号）',
    }
    
    # 虚拟商品的自动化思路（很简单）
    AUTOMATION = """
    1. 监控低价渠道（拼多多、淘宝特价）
    2. 发现好价就下单
    3. 收到码后，立即在闲鱼发布
    4. 定价 = 成本 + 5%
    5. 有人买立即发码
    
    完全自动化很难，但半自动很简单
    """